/**
 * Get position data by name (case-insensitive with variations)
 * @param {string} positionName - Name of the position
 * @returns {object|null} Position data or null if not found
 */
export const getPositionData = (positionName) => {
    if (!positionName) return null;
    
    // Try exact match first
    if (CareerPositionData[positionName]) {
        return CareerPositionData[positionName];
    }
    
    // Try case-insensitive match
    const keys = Object.keys(CareerPositionData);
    const normalizedInput = positionName.toLowerCase().trim();
    
    for (const key of keys) {
        if (key.toLowerCase() === normalizedInput) {
            return CareerPositionData[key];
        }
    }
    
    // Try matching with "And" variations
    const withAnd = positionName.replace(/\s+and\s+/gi, ' And ');
    if (CareerPositionData[withAnd]) {
        return CareerPositionData[withAnd];
    }
    
    return null;
};

/**
 * Get all position names
 * @returns {array} Array of position names
 */
export const getAllPositionNames = () => {
    return Object.keys(CareerPositionData);
};

export default CareerPositionData;

